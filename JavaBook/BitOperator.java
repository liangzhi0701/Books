public class BitOperator {
	public static void main(String args[]) {
		int op1 = 6;
		int op2 = 2;

		System.out.println(~ op1);
		System.out.println(op1 | op2);
		System.out.println(op1 & op2);
		System.out.println(op1 >> op2);
		System.out.println(op1 << op2);
		System.out.println(op1 >>>op2);
	}
}